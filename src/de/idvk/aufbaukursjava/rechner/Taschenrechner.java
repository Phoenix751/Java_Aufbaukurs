package de.idvk.aufbaukursjava.rechner;

public class Taschenrechner {

    public static int add(int a,
                          int b) {
        return a + b;
    }

    public static int mult(int a,
                           int b) {
        return a * b;
    }

    public static int sub(int a,
                          int b) {
        return a - b;
    }

    public static int div(int a,
                          int b) {
        return a / b;
    }


}
